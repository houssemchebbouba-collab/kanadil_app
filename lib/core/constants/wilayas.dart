  /// الحصول على رقم الولاية من اسمها
  static int? getWilayaNumber(String wilayaName) {
    final index = wilayas.indexOf(wilayaName);
    return index != -1 ? index + 1 : null;
  }

  /// الحصول على اسم الولاية من رقمها
  static String? getWilayaName(int number) {
    if (number < 1 || number > wilayas.length) return null;
    return wilayas[number - 1];
  }
